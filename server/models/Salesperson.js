const { Schema, model } = require("mongoose");

const salespersonSchema = new Schema({
    first_name: {
        type: String,
        required: true,
    },
    last_name: {
        type: String,
        required: true,
    },
    phone_number: {
        type: String,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    }
});

const Salesperson = model("Salesperson", salespersonSchema);

module.exports = Salesperson;
